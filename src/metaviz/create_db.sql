USE MetaViz;
DROP TABLE IF EXISTS images;
CREATE TABLE images (
	hash char(64) NOT NULL PRIMARY KEY,
	File_Size varchar(10),
	File_Permissions char(9),
	File_Type varchar(10),
	MIME_Type varchar(20),
	Exif_Byte_Order varchar(50),
	Make varchar(20),
	Camera_Model_Name varchar(50),
	Orientation varchar(30),
	X_Resolution int,
	Y_Resolution int,
	Resolution_Unit varchar(20),
	Modify_Date datetime,
	Y_Cb_Cr_Positioning varchar(20),
	Exposure_Time varchar(15),
	F_Number decimal(5,1),
	Exif_Version int(4),
	Date1Time_Original datetime,
	Create_Date datetime,
	Components_Configuration varchar(20),
	Shutter_Speed_Value varchar(4),
	Aperture_Value decimal(3,1),
	Brightness_Value float,
	Max_Aperture_Value decimal(3,1),
	Light_Source varchar(20),
	Flash varchar(50),
	Focal_Length varchar(10),
	Warning varchar(50),
	Flashpix_Version int(4),
	Color_Space varchar(10),
	Exif_Image_Width int,
	Exif_Image_Height int,
	Interoperability_Index varchar(50),
	Interoperability_Version int(4),
	Scene_Type varchar(50),
	Custom_Rendered varchar(20),
	Exposure_Mode varchar(50),
	White_Balance varchar(50),
	Digital_Zoom_Ratio int,
	Scene_Capture_Type varchar(50),
	Contrast varchar(50),
	Saturation varchar(50),
	Sharpness varchar(50),
	GPS_Version_ID varchar(10),
	GPS_Latitude_Ref varchar(5),
	GPS_Longitude_Ref varchar(4),
	GPS_Altitude_Ref varchar(50),
	GPS_Time_Stamp time,
	GPS_Map_Datum varchar(50),
	GPS_Processing_Method varchar(50),
	GPS_Date_Stamp date,
	Compression varchar(50),
	Thumbnail_Offset int,
	Thumbnail_Length int,
	Image_Width int,
	Image_Height int,
	Encoding_Process varchar(50),
	Bits_Per_Sample int,
	Color_Components int,
	Y_Cb_Cr_Sub_Sampling varchar(50),
	Aperture decimal(3,1),
	GPS_Altitude varchar(50),
	GPS_Date1Time datetime,
	GPS_Latitude varchar(50),
	GPS_Longitude varchar(50),
	GPS_Position varchar(50),
	Image_Size varchar(20),
	Shutter_Speed varchar(10),
	Thumbnail_Image varchar(200)
	);
